<template>
	<div>
		<p>author : {{ author }}</p>
		<p>title : {{ title }}</p>
	</div>
</template>

<script>
import { reactive, toRefs, toRef } from 'vue';

export default {
	setup() {
		const book = reactive({
			author: 'Vue Team',
			year: '2020',
			title: 'Vue 3 Guide',
			description: '당신은 이 책을 지금 바로 읽습니다 ;)',
			price: '무료',
		});
		// const { author, title } = toRefs(book);
		const author = toRef(book, 'author'); //just want only one attrs
		const title = toRef(book, 'title'); //just want only one attrs
		return {
			author,
			title,
			book,
		};
	},
};
</script>

